def maiorpeso():
    peso1 = int(input("digite o primeiro peso->"))
    peso2 = int(input("digiteo segundo peso->"))
    peso3 = int(input("digite o terceiro peso->"))
    peso4 = int(input("digite o quarto peso->"))
    peso5 = int(input("digite o quinto peso->"))
    
    max(peso1, peso2, peso3, peso4, peso5)
    min(peso1, peso2, peso3, peso4, peso5)